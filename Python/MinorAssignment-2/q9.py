num = int(input("Enter an integer: "))
remainder = num % 5
if remainder == 0:
    print("Remainder is 0")
elif remainder == 1:
    print("Remainder is 1")
elif remainder == 2:
    print("Remainder is 2")
elif remainder == 3:
    print("Remainder is 3")
elif remainder == 4:
    print("Remainder is 4")